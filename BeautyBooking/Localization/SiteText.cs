        public static string Get(string key, string language)
        {
            var lang = (language ?? "da").ToLowerInvariant();
            var selected = lang switch
            {
                "en" => En,
                "fr" => Fr,
                "de" => De,
                "ar" => Ar,
                "fa" => Fa,
                "zh" => Zh,
                _ => Da
            };

            if (selected.TryGetValue(key, out var value))
            {
                return value;
            }

            if (Da.TryGetValue(key, out var fallback))
            {
                return fallback;
            }

            return key;
        }
